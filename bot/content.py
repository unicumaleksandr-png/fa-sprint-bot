def day_status(log: dict) -> str:
    icons = {True: "✅", False: "⬜"}
    lines = [
        f"{icons[log.get('fa_done',      False)]}  Финансовый анализ",
        f"{icons[log.get('ifrs_done',    False)]}  МСФО",
        f"{icons[log.get('excel_done',   False)]}  Excel / Power BI",
        f"{icons[log.get('english_done', False)]}  Английский",
        f"{icons[log.get('sport_done',   False)]}  Спорт",
    ]
    done = log.get("total_done", 0)
    tail = ("🔥 Отличный прогресс!" if done >= 4
            else "👊 Продолжай — ещё есть время." if done >= 2
            else "⚡ Начни прямо сейчас.")
    return f"📋 *Сегодня: {done}/5*\n\n" + "\n".join(lines) + f"\n\n{tail}"


def weekly_review(rows: list, week: int, streak: int, best: int) -> str:
    total_done = sum(r["total"] for r in rows)
    total_possible = len(rows) * 5
    pct = int(total_done / total_possible * 100) if total_possible else 0
    perfect = sum(1 for r in rows if r["total"] == 5)

    task_keys = ["fa", "ifrs", "excel", "english", "sport"]
    task_labels = {
        "fa": "Фин. анализ", "ifrs": "МСФО", "excel": "Excel",
        "english": "Английский", "sport": "Спорт",
    }
    totals  = {k: sum(r[k] for r in rows) for k in task_keys}
    worst   = min(totals, key=totals.get)
    best_t  = max(totals, key=totals.get)
    n       = len(rows)
    nw      = min(4, week + 1)

    msg = (
        f"📈 *ИТОГИ НЕДЕЛИ {week}*\n\n"
        f"Выполнено: *{total_done}/{total_possible}* ({pct}%)\n"
        f"Идеальных дней: *{perfect}/{n}*\n"
        f"Серия: *{streak} дн.* · Рекорд: *{best} дн.*\n\n"
        f"━━━━━━━━━━━━━━━━━━━━\n"
        f"💪 Лучшее: {task_labels[best_t]} — {totals[best_t]}/{n}\n"
        f"⚠️ Зона роста: {task_labels[worst]} — {totals[worst]}/{n}\n\n"
        f"🎯 Следующая неделя {nw}: {WEEK_THEMES[nw]}\n\n"
    )

    if pct >= 80:
        msg += "Сильная неделя. Повышаю нагрузку — добавляется кейс.\nРаботай."
    elif pct >= 60:
        msg += "Хорошая неделя. На следующей: минимум 80%."
    else:
        msg += (
            "Сложная неделя. Что мешало — ты знаешь.\n"
            "На следующей: один пропуск — максимум."
        )
    return msg


